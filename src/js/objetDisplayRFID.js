function assiete1Verif() {
    let checkBox = document.getElementById('buttonAssiette1');
    if (checkBox.checked) {
        document.getElementById('assiete1ID').style.opacity = '1';
    } else {
        document.getElementById('assiete1ID').style.opacity = '0';
    }
}

